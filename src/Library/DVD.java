package Library;
public class DVD extends Item {
    // Constructor
    public DVD(String title, String author, String publication, int year, String language, String subject, String status) {
        this.title = title;
        this.author = author;
        this.publication = publication;
        this.year = year;
        this.language = language;
        this.subject = subject;
        this.status = status;
    }
}
